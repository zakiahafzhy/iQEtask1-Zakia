package Soal3;

public class Kalkulator {
    public static int jumlah (int a, int b) {
        int calc = a + b;
        return calc;
    }
    public static int kurang (int a, int b) {
        int calc = a - b;
        return calc;
    }
    public static int kali(int a, int b) {
        int calc = a * b;
        return calc;
    }
    public static double bagi(double a, double b) {
        double calc = a / b;
        return calc;
    }
}
